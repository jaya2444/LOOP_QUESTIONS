# Puja Na. First 10 Even numbers
# b. First 10 Odd numbers
# c. First 10 Natural numbers
# d. First 10 Whole numbers

# EVEN NUMBERS...........
i=2
while i<=10:
    print(i)
    i=i+2

i=1
while i<=10:
    if i%2==0:
        print(i)
    i=i+1

# ODD NUMBERS...............
i=1
while i<10:
    if i%2!=0:
        print(i)
    i=i+1

i=1
while i<=10:
    print(i)
    i=i+2


# NATURAL NUMBERS.................
i=1
while i<=10:
    print(i)
    i=i+1


# WHOLE NUMBERS...........
i=0
while i<=10:
    print(i)
    i=i+1

